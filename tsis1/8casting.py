x = int(1)   # x will be 1
y = int(2.8) # y will be 2
z = int("3") # z will be 3
print(x, y, z)

a = float(1)     # a will be 1.0
b = float(2.8)   # b will be 2.8
c = float("3")   # c will be 3.0
d = float("4.2") # d will be 4.2
print(a, b, c, d)

n = str("s1") # n will be 's1'
m = str(2)    # m will be '2'
l = str(3.0)  # l will be '3.0'
print(n, m, l)